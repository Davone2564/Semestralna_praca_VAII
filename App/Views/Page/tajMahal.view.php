    <h1 class="my-article-heading">Tádž Mahal, symbol lásky muža a ženy, patrí medzi sedem divov sveta</h1>
</div>
<img src="public/images/taj_mahal.jpg" alt="Taj Mahal" class="align-center">
<div>
    <p class="my-article-top-text">
        Počas druhej svetovej vojny bolo v roku 1942 nad mauzóleom postavené obrovské lešenie, ktoré ho chránilo
        pred prípadnými leteckými útokmi nemeckej Luftwaffe a japonského letectva.
    </p>
</div>
<p class="my-body-text">
    Zhruba 2200 rokov po tom, čo helenistický filozof a spisovateľ Filón Byzantský (pribl. 280-220 pred n.l.)
    opísal starovekých sedem divov sveta, bolo 7. júla 2007 v portugalskom Lisabone vyhlásených nových sedem divov sveta.
    Medzi nimi aj Tádž Mahal.
</p>
<p class="my-body-text">
    Tádž Mahal (Koruna paláca) je mauzóleum považované za symbol oddanosti muža k žene.
    V Ágre - pri rieke Jamuna v indickom štáte Uttarpradéš, 200 kilometrov od hlavného mesta
    Indie Naí Dillí - ho dal postaviť mogul, cisár Šáhdžahán, žijúci v rokoch 1592-1666.
    Uctil si tak pamiatku svojej manželky.
</p>
<p class="my-body-text">
    Ardžumand Banu, známa aj pod menom Mumtáz Mahal (vyvolená paláca), zomrela v roku 1629 (niektoré pramene uvádzajú aj rok 1631)
    po 17-ročnom manželstve pri pôrode štrnásteho dieťaťa vo veku 36 rokov.
    Pretože bola Šáhdžahánovi nielen milovanou ženou, ale aj politickou radkyňu, rozhodol sa, že jej postaví monumentálny pomník.
</p>
<p class="my-body-text">
    Tádž Mahal začali stavať v roku 1632. Výstavba mauzólea na mramorovom podstavci trvala sedem rokov.
    Pridružené budovy boli dokončené v roku 1643 a práce na dekorácii pokračovali až do roku 1647.
    Celková výstavba komplexu nakoniec trvala 22 rokov. Pracovalo na nej 20.000 remeselníkov a pri doprave mramoru zo vzdialenosti 300 kilometrov
    či dovoze ďalších stavebných materiálov používali stavitelia až 1000 slonov.
</p>
<p class="my-body-text">
    Povrch mauzólea zdobia polodrahokamy a drahokamy, kaligrafická výzdoba je z čierneho mramoru.
    yrkys pochádza z Perzie, karneol (odroda kremeňa) z Bagdadu a malachit z ruských baní.
    Na dekoráciu sa použilo zlato, lazurit, chalcedón, achát, jaspis, koral, onyx a ametist.
</p>
<p class="my-body-text">
    Hlavným architektom výstavby komplexu bol Ustád Ahmad Láhaurí,
    ktorý pri budovaní komplexu dokázal spojiť perzské, turecké, indické a islamské architektonické štýly.
</p>
<p class="my-body-text">
    Komplex stavieb Tádž Mahalu sa rozprestiera na ploche viac ako 17 hektárov.
    Jeho súčasťou je štvorcová symetrická záhrada, typická pre mogulský štýl, symbolizujúci rajskú záhradu.
    Cez jej stred sa tiahne vodný kanál, v ktorom sa mauzóleum odráža.
</p>
<p class="my-body-text">
    Osemuholníková budova mauzólea stojí na vyvýšenej štvorcovej plošine s rozlohou 100 x 100 metrov,
    v ktorej rohoch stoja 40 metrov vysoké minarety.
    Mauzóleu dominuje kupola cibuľovitého tvaru, ktorú doplňujú štyri menšie kupoly.
</p>
<p class="my-body-text">
    Farba mauzólea sa mení podľa dopadajúceho svetla: ráno býva ružovkasté, podvečer biele,
    v noci za mesačného svitu zase vyzerá, akoby bolo zo zlata.
</p>
<p class="my-body-text">
    Kým v 30. rokoch 19. storočia neúspešne plánoval guvernér Indie a Bengálska William Bentick mramor z mauzólea predať na aukcii vo Veľkej Británii,
    koncom 19. storočia sa jeho neskorší nástupca, guvernér Indie George Curzon, zaslúžil o to, aby bol Tádž Mahal zreštaurovaný a vyhlásený za národnú pamiatku.
</p>
<p class="my-body-text">
    Počas druhej svetovej vojny bolo v roku 1942 nad mauzóleom postavené obrovské lešenie,
    ktoré ho chránilo pred prípadnými leteckými útokmi nemeckej Luftwaffe a japonského letectva.
</p>
<p class="my-body-text">
    V roku 1983 bol Tádž Mahal zapísaný do Zoznamu svetového kultúrneho a prírodného dedičstva Organizácie Spojených národov pre vzdelávanie,
    vedu a kultúru (UNESCO).
</p>
<p class="my-body-text">
    Žiaľ, v súčasnosti dochádza vplyvom znečistenia ovzdušia k žltnutiu a tým aj znehodnocovaniu pôvodne bieleho mramoru na mauzóleu,
    keďže sa komplex nachádza v blízkosti rozvíjajúcej sa priemyselnej štvrte.
</p>